#include "stm32f4xx_it.h"

//SysTick Interpreter
extern int sys_delay;

/**
  * @brief  This function handles SysTick Handler.
  * @param  None
  * @retval None
  */
void SysTick_Handler(void)
{
	if (sys_delay!=0)
	{
		sys_delay--;
	}
}

